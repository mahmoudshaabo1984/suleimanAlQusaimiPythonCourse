# هذا البرنامج يطلب من المستخدم إدخال اسمه ومبلغا من المال، ثم يقوم بطباعة ترحيب بالمستخدم وإضافة قيمة للمبلغ.

# طلب إدخال الاسم من المستخدم عبر دالة الإدخال
name = input("أدخل اسمك فضلا: ")

# عرض رسالة ترحيبية باستخدام دمج النصوص
print("مرحبا بك يا " + name)

# طلب إدخال مبلغ مالي كقيمة نصية وتحويله فورا إلى رقم صحيح
amount = int(input("أدخل المبلغ المالي الذي وجدته من أجل المهمة: "))

# إضافة 5 للمبلغ وطباعة النتيجة النهائية
print("إجمالي المبلغ بعد إضافة 5 هو:", amount + 5)
