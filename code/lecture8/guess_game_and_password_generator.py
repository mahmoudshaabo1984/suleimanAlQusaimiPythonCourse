# البرنامج النهائي: اللعبة المتكاملة لتخمين الرقم السري وبرنامج مولد كلمات المرور المدمج

import random
from random import choice

print("--- مرحباً بك في البرنامج المزدوج للمحاضرة الثامنة ---")
mode = input("اختر 1 للعبة تخمين الرقم، أو 2 لمولد كلمات المرور: ")

if mode == "1":
    # برنامج لعبة تخمين الرقم السري
    print("\n--- لعبة تخمين الرقم (من 1 إلى 10) لديك 5 محاولات ---")
    secret = random.randint(1, 10)
    count = 1
    
    while count <= 5:
        # إدخال محاولة المستخدم وتحويلها لرقم
        answer = int(input(f"المحاولة {count} - خمن الرقم: "))
        
        # التوجيه بناء على الرقم
        if answer == secret:
            print(f"ممتاز! لقد توصلت إلى الحل الصحيح خلال {count} محاولة/محاولات.")
            break
        elif answer > secret:
            print("خطأ، فكر في رقم أصغر من هذا.")
        elif answer < secret:
            print("خطأ، فكر في رقم أكبر من هذا.")
            
        count += 1
    else:
        # تنفذ فقط إذا لم يتم كسر الحلقة بالفوز
        print(f"للأسف، لقد انتهت محاولاتك الخمس! الإجابة الصحيحة كانت: {secret}")

elif mode == "2":
    # برنامج توليد الكلمات السرية الآمنة
    print("\n--- مولد كلمات المرور العشوائية القوية ---")
    # جميع الرموز المقبولة من أحرف وأرقام وعلامات
    chars = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%^&*()_+"
    
    # تحديد تعقيد الكلمة عبر طولها
    length = int(input("ما هو طول كلمة المرور الذي تريده؟ "))
    
    password = ""
    count = 1
    # البناء التدريجي لكلمة المرور حرفاً بعد حرف
    while count <= length:
        password += choice(chars)
        count += 1
        
    print(f"كلمة المرور العشوائية المقترحة بقوة هي: {password}")

else:
    print("خيار غير صحيح لنمط التشغيل.")
