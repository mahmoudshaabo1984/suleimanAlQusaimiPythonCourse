# البرنامج النهائي: حساب المتوسط الحسابي بناء على أرقام يدخلها المستخدم باستخدام القوائم وحلقة التكرار for

# إنشاء قائمة فارغة لتخزين الأرقام المضافة
numbers = []

# حلقة تكرارية مرتها خمس مرات لطلب إدخال 5 أرقام
for i in range(5):
    # طلب رقم من المستخدم وتذكيره برقم المحاولة
    number = input(f"اكتب الرقم {i + 1}: \n")
    
    # تحويل النص المدخل إلى عدد صحيح
    number = int(number)
    
    # إضافة الرقم حديث الإدخال إلى القائمة
    numbers.append(number)

# حساب المجموع الكلي وتقسيمه على عدد الأرقام الكلي (طول القائمة) للحصول على المتوسط
av = sum(numbers) / len(numbers)

# طباعة المتوسط الحسابي النهائي للدرجات المدخلة
print("المتوسط الحسابي للأرقام المدخلة هو:", av)
