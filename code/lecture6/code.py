# إنشاء القوائم والوصول لعناصرها الأساسية
members = ["ahmed", "ali", "khalid", "mohammed"]
print(len(members))       # طباعة طول القائمة
print(members[3])         # الوصول للعنصر الرابع في القائمة
print(members[-1])        # الوصول للعنصر الأخير
print(members[1:])        # التقطيع بدءا من العنصر الثاني للنهاية
print(members[::-1])      # عكس القائمة بالكامل

# إضافة وحذف العناصر والتعديل عليها تباعا
members.append("masood")          # إضافة في آخر القائمة
members.insert(0, "sulaiman")     # إضافة عنصر في الموقع الأول
members.remove("sulaiman")        # حذف العنصر بحسب قيمته النصية
members.pop(2)                    # حذف العنصر بحسب موقعه
members[0] = "mostafa"            # تعديل قيمة العنصر الأول بشكل مباشر

# الترتيب والعد الإحصائي
print(members.count("ahmed"))     # عدد مرات تكرار اسم أحمد في القائمة
members.sort()                    # ترتيب القائمة أبجديا

# الدوال الحسابية على قوائم الأرقام واستنساخ القوائم (النسخ الآمن)
grades = [90, 87, 81, 98, 96, 99]
print(sum(grades))                # حساب مجموع الدرجات
print(min(grades))                # العثور على أقل درجة
print(max(grades))                # العثور على أعلى درجة
grades_copy = grades.copy()       # أخذ نسخة حقيقية من القائمة لتفادي التعديل العشوائي على الأصل بدلا من المتغير المرجعي

# الحلقات التكرارية (For Loops)
# المرور المباشر على القائمة مع التحقق من الأعداد الفردية والزوجية
for i in [1, 2, 3, 4, 5]:
    if i % 2 == 0:
        print(f"الرقم {i} هو عدد زوجي")
    else:
        print(f"الرقم {i} هو عدد فردي")

# استخدام الدالة range() للترقيم الإضافي عند الطباعة بدلا من القيمة نفسها
for index in range(len(members)):
    print(f"{index + 1}. {members[index]}")
