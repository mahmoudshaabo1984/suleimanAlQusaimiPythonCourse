# البرنامج النهائي: نظام بحث عن درجات الطلاب يعمل باستمرار باستخدام while True و القوائم المتداخلة

# تجهيز قاعدة بيانات الطلاب (قائمة داخلها قوائم فرعية للاسم والدرجة)
students = [["ali", 97], ["mohammed", 92], ["ahmed", 94], ["isa", 96]]

# حلقة لا نهائية ليعمل البرنامج باستمرار وللطلب من المستخدم مراراً
while True:
    # طلب الاسم من المستخدم
    name = input("أدخل اسم الطالب الذي تريد معرفة درجته (أو 'q' للخروج من البرنامج): ")
    
    # شرط الخروج من البرنامج بناء على أمر المستخدم للإنهاء
    if name == "q":
        print("...جاري إغلاق البرنامج")
        break  # كسر حلقة while الرئيسية وإغلاق البرنامج
    
    # بدء عملية البحث داخل القائمة المتداخلة بمجرد أن المستخدم أدخل اسم وليس 'q'
    for s in students:
        # فحص: هل الاسم المعطى في القائمة الفرعية يطابق الاسم المدخل؟
        if s[0] == name:
            print(f"درجة الطالب {name} هي {s[1]}")
            # التوقف عن البحث لأننا وجدنا الطالب، والعودة إلى حلقة while الرئيسية
            break 
    else:
        # حالة عدم العثور (for...else): تنفذ فقط إذا بحثنا في كامل القائمة ولم نجد الاسم
        print(f"الاسم '{name}' غير موجود في قاعدة البيانات")
