# مثال 1: التحقق من اسم المستخدم باستخدام الجمل الشرطية (if و else)

# تعريف اسم المستخدم الصحيح مسبقا
name = "محمود"

# طلب إدخال اسم المستخدم
userName = input("أدخل اسم المستخدم: ")

# الجملة الشرطية للتحقق من مطابقة المدخل مع الاسم المسجل
if name == userName:
    print("تم تسجيل الدخول بنجاح")
    print("مرحبا بك في البرنامج")
else:
    print("خطأ: اسم المستخدم غير صحيح")
