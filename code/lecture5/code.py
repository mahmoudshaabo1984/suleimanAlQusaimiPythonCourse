# دوال النصوص الأساسية وتغيير حالة الأحرف
s = "welcome to this course"
print(s.upper())      # تكبير كل الأحرف
print(s.lower())      # تصغير كل الأحرف
print(s.capitalize()) # تكبير أول حرف من الجملة فقط
print(s.title())      # تكبير أول حرف من كل كلمة

# التحقق والاستبدال
s_arabic = "مرحبا بكم في دورة الجافا"
print(s_arabic.replace("الجافا", "البايثون")) # الاستبدال (تغيير الجافا بالبايثون)

is_numbers = "12345".isdigit() # للتحقق مما إذا كان النص يتكون من أرقام فقط

# الفهارس والتقطيع (Indexing & Slicing)
text = "السلام عليكم ورحمة الله وبركاته"
print(text[0])       # طباعة أول حرف عند الفهرس 0
print(text[-1])      # طباعة آخر حرف عند الفهرس -1
print(text[7:12])    # التقطيع من الفهرس 7 إلى 11
print(text[::-1])    # قلب النص وعكسه باستخدام الخطوة التراجعية

# حساب الطول والبحث داخل النصوص
print(len(text))     # طباعة الطول الإجمالي للنص
start = text.find("رحمة") # البحث عن موقع بداية كلمة محددة
print("تبدأ كلمة 'رحمة' عند الفهرس:", start)

