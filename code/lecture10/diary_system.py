# البرنامج النهائي للمحاضرة العاشرة: نظام المذكرات المصغر الذي يكتب مدخلات المستخدم للملف فورياً ثم يقرأها بالكامل

print("=== برنامج المذكرات اليومية النصية ===")
# فتح الملف في وضع الإضافة، بحيث لا نفقد المذكرات والذكريات القديمة
file = open("diary.txt", "a", encoding="utf-8")

while True:
    note = input("اكتب مذكرتك الجديدة (أو اضغط زر الإدخال للتوقف والإغلاق): ")
    if note == "":
        break
    # كتابة المفكرة مع نزول سطر جديد استعداداً للمفكرة القادمة مستقبلا
    file.write(note + "\n")
    print("تم حفظ المذكرة بنجاح.")

# إغلاق الملف الإجباري بعد الانتهاء من الإضافة ليتم حفظ التعديلات بشكل فعلي
file.close()

print("\n--- سجل المذكرات بالكامل ---")
# فتح ذات الملف بوضع القراءة لعرض محتوياته للمستخدم
try:
    read_file = open("diary.txt", "r", encoding="utf-8")
    content = read_file.read()
    if content.strip() == "":
        print("لاتوجد مذكرات مسجلة.")
    else:
        print(content)
    read_file.close()
except FileNotFoundError:
    # التقاط الخطأ في حال حاول المستخدم قراءة السجل قبل إضافة أي شيء وكان الملف غير موجود
    print("ملف المذكرات غير موجود بعد.")
