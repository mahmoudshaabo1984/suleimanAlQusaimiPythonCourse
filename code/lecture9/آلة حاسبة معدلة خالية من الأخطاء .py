





# اسم البرنامج: الآلة الحاسبة البسيطة (Simple Calculator)
# حلقة تكرار لا نهائية لضمان عمل البرنامج باستمرار    
while True:
    # استقبال المدخلات كنصوص أولاً
    x = input("أدخل الرقم الأول: ")
    o = input("أدخل العملية الحسابية (+-*/): ")
    y = input("أدخل الرقم الثاني: ")
    # المنطقة الخطرة الأولى: تحويل النص إلى رقم
    # نستخدم try لنقول: "جرب تنفذ هذا الكود، وراقب إذا حدث خطأ"
    try:
        x = int(x)
        y = int(y)
    # except: إذا حدث خطأ من نوع ValueError (قيمة غير صحيحة)، نفذ هذا الكود البديل
    except ValueError:
        print("خطأ: الرجاء إدخال أرقام صحيحة فقط!")
        # continue: تجاهل ما تبقى من الكود وعد لبداية الحلقة while فوراً
        continue
    # تنفيذ العمليات الحسابية
    if o == "+":
        print(x + y)
    elif o == "-":
        print(x - y)
    elif o == "*":
        print(x * y)
    elif o == "/":
        # المنطقة الخطرة الثانية: القسمة
        # نجرب القسمة، لكننا نحذر من القسمة على صفر
        try:
            print(x / y)
        # إذا حاول المستخدم القسمة على صفر (ZeroDivisionError)
        except ZeroDivisionError:
            print("خطأ: لا يمكن القسمة على صفر!")
    # سؤال للاستمرار أو الخروج
    ask = input("هل تريد الاستمرار؟ (y/n): ")
    if ask != 'y':
        break
