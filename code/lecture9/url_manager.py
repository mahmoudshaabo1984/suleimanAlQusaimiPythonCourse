# البرنامج النهائي: نظام مخزن الروابط التفاعلي باستخدام القواميس ومعالجة الأخطاء الذكية

import webbrowser

# إنشاء قاموس فارغ يمثل قاعدة بيانات الروابط في الذاكرة الحية
urls = {}
print("=== مرحباً بك في مخزن الروابط الذكي (المحاضرة التاسعة) ===")

# حلقة تفاعلية مستمرة
while True:
    option = input("\n1: فتح رابط محفوظ\n2: إضافة رابط جديد\nq: للخروج\nاختر رقماً من القائمة: ")

    if option == "1":
        # عرض الروابط المتوفرة وتجهيزها
        keys = list(urls.keys())
        if not keys:
            print("(!) لا توجد أي عناصر مضافة بعد.")
        else:
            print("الروابط المتوفرة لديك الآن:")
            for key in keys:
                print("-", key)
                
            name = input("اكتب اسم الموقع بالتحديد الذي تريد فتحه: ")
            
            # اقتناص خطأ عدم تواجد المفتاح المعطى لتجنب إيقاف البرنامج
            try:
                link = urls[name]
                print(f"جاري فتح موقع {name}...")
                webbrowser.open(link)
            except KeyError:
                print("(!) خطأ: هذا الموقع غير مسجل في القاموس مسبقاً.")
                
    elif option == "2":
        key = input("اكتب اسماً للموقع ليكون المفتاح (مثال: google): ")
        link = input("ألصق الرابط كاملاً هنا: ")
        # الإضافة المباشرة للقاموس
        urls[key] = link
        print(f"تمت إضافة '{key}' بنجاح.")
        
    elif option.lower() == "q":
        print("جاري إغلاق مخزن الروابط...")
        break
    else:
        print("اختيار غير صالح، الرجاء المحاولة مجددا.")
