# تعريف دالة لحساب المتوسط الحسابي لقائمة من الأرقام
def average(l):
    # حساب مجموع الأرقام في القائمة
    s = sum(l)
    # حساب عدد عناصر القائمة
    length = len(l)
    # إرجاع ناتج قسمة المجموع على عدد العناصر (المتوسط)
    return s / length

# قائمة من الأرقام لتجربة الدالة
numbers = [5, 4, 2, 6, 9, 10, 30, 20, 50]

# استدعاء الدالة وطباعة نتيجة المتوسط
av = average(numbers)
print(av)