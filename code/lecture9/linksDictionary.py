






# linkDictionary
# إنشاء قاموس (Dictionary) يحتوي على أسماء مواقع وروابطها
# القاموس يتكون من مفتاح (Key) وقيمة (Value)
urls = {
    "google": "www.google.com", 
    "youtube": "www.youtube.com", 
    "twitter": "www.twitter.com"
}
# الوصول إلى قيمة معينة باستخدام المفتاح الخاص بها (طباعة رابط تويتر)
print(urls["twitter"])
# إضافة عنصر جديد إلى القاموس (مفتاح: blindtec، قيمة: رابطه)
urls["blindtec"] = "www.blindtec.net"
# طباعة القاموس كاملاً لرؤية العنصر الجديد المضاف
print(urls)

# حذف عنصر من القاموس باستخدام الدالة pop (حذف يوتيوب)
# الدالة pop تقوم بحذف العنصر وإرجاع قيمته (لذلك يمكن طباعتها)
print(urls.pop("youtube"))
# طباعة القاموس مرة أخرى للتأكد من حذف يوتيوب
print(urls)
# الحصول على جميع المفاتيح (Keys) الموجودة في القاموس
# النتيجة هنا تكون بنوع خاص يسمى dict_keys
print(urls.keys())
# تحويل المفاتيح إلى قائمة عادية (List) لكي نستطيع التعامل معها بالترتيب
keys = list(urls.keys())
# طباعة القائمة الجديدة التي تحتوي على المفاتيح فقط
print(keys)

# الوصول إلى أول عنصر في قائمة المفاتيح (باستخدام الفهرس 0)
print(keys[0])
# الوصول إلى آخر عنصر في قائمة المفاتيح (باستخدام الفهرس -1)
print(keys[-1])