# البرنامج الثاني: جدول الضرب باستخدام الحلقات المتداخلة
# Program 2: Multiplication table using nested while loops
# فتح ملف باسم table.txt بوضع الكتابة w
# سيتم كتابة جدول الضرب كاملاً داخل هذا الملف
file = open("table2.txt", "w")
# تعيين قيمة البداية للرقم الأول (الحلقة الخارجية)
x = 1
# تعيين قيمة البداية للرقم الثاني (الحلقة الداخلية)
# نجعل y تساوي x لأن جدول الضرب يبدأ من الرقم نفسه
# مثلاً: جدول 3 يبدأ من 3 × 3 وليس 3 × 1
y = x
# الحلقة الخارجية: تتحكم بالرقم الأول (من 1 إلى 12)
while x <= 12:
    # الحلقة الداخلية: تتحكم بالرقم الثاني (من x إلى 12)
    # تتكرر بالكامل في كل دورة من الحلقة الخارجية
    while y <= 12:
        # كتابة عملية الضرب في الملف باستخدام f-string
        # مثال: "3 x 4 = 12\n"
        # الحرف \n ينقلنا إلى سطر جديد بعد كل عملية
        file.write(f"{x} x {y} = {x*y}\n")
        # زيادة الرقم الثاني بمقدار 1 للانتقال للعملية التالية
        y = y + 1
    # بعد انتهاء الحلقة الداخلية: زيادة الرقم الأول بمقدار 1
    x = x + 1
    # إعادة تعيين y لتساوي x الجديدة
    # هذا يمنع تكرار العمليات (مثلاً 3×4 و 4×3)
    y = x
# إغلاق الملف بعد كتابة كامل جدول الضرب
file.close()

print("تم إنشاء ملف table2.txt بنجاح وكتابة جدول الضرب فيه!")
print("افتح الملف table2.txt لمشاهدة النتيجة.")
