# البرنامج النهائي: البحث المتقدم في النصوص واستخدام f-strings لطباعة النتيجة التفصيلية
# النص الذي سيتم البحث فيه
storedText = "السلام عليكم ورحمة الله وبركاته مرحبا بكم في دورة البيثون"
# طلب الكلمة المراد البحث عنها من المستخدم
searchWord = input("أدخل عبارة البحث\n")
# البحث عن الموقع الأول للكلمة باستخدام الدالة find
startPoint = storedText.find(searchWord)
# التحقق مما إذا كانت الكلمة موجودة أم لا (طالما لا ترجع -1)
if startPoint == -1:
    print("للأسف، لم يتم العثور على النص المطلوب")
else:
    # حساب نقطة النهاية للكلمة المبحوث عنها بجمع موقع البداية مع طول الكلمة
    stopPoint = startPoint + len(searchWord)
    # استخدام f-strings لطباعة النتيجة بشكل منسق مع مواقع الكلمة وطولها الفعلي
    print(f"تم العثور على عبارة البحث بين الموقعين {startPoint} و {stopPoint}")
    print(f"يبلغ طول العبارة التي بحثت عنها {len(searchWord)}")
    




