# عمليات الملفات النصية (الكتابة، الإلحاق، القراءة)
# جميع استدعاءات open() تتضمن encoding="utf-8" لدعم اللغة العربية بشكل صحيح
# البرنامج الأول الكتابة على الملفات وتعديلها

# ===== الجزء الأول: الكتابة بوضع "w" =====

# فتح ملف data.txt بوضع الكتابة "w" مع ترميز utf-8
# إذا لم يكن الملف موجوداً، يقوم بايثون بإنشائه تلقائياً
# إذا كان موجوداً، يتم حذف كل المحتوى القديم
file = open("data_1.txt", "w", encoding="utf-8")

# كتابة رسالة ترحيبية داخل الملف باستخدام دالة write()
file.write("السلام عليكم ورحمة الله وبركاته")
# إغلاق الملف لحفظ التغييرات — هذه الخطوة ضرورية
file.close()


# إذا حاولنا الكتابة بعد الإغلاق، سيظهر هذا الخطأ:
# ValueError: I/O operation on closed file.
# file.write("some text")  # <-- هذا سيتسبب في خطأ
# فتح الملف مجدداً بوضع "w" — هذا يمسح كل ما كُتب سابقاً
file = open("data_1.txt", "w", encoding="utf-8")

# كتابة كلمة "python" — تحل محل الرسالة القديمة
file.write("python")
# إغلاق الملف
file.close()


# فتح الملف مجدداً بوضع "w" — يمسح المحتوى مجدداً
file = open("data_1.txt", "w", encoding="utf-8")

# كتابة النص الأول
file.write("هذا نص")

# كتابة النص الثاني مباشرة بعد الأول (بدون سطر جديد بينهما)
file.write("هذا نص آخر")
# إغلاق الملف
file.close()

# فتح الملف بوضع الإلحاق "a"
# هذا الوضع يضيف نصاً جديداً في النهاية دون حذف المحتوى القديم
file = open("data_1.txt", "a", encoding="utf-8")

# إضافة نص جديد في نهاية الملف (بدون سطر جديد)
file.write("سليمان القسيمي")

# إغلاق الملف
file.close()

# فتح الملف بوضع الإلحاق مجدداً
file = open("data_1.txt", "a", encoding="utf-8")

# إضافة نص في سطر جديد باستخدام \n
# الحرف \n يعني: انتقل إلى سطر جديد قبل كتابة النص
file.write("\nسليمان القسيمي")

# إغلاق الملف
file.close()
# فتح الملف بوضع الإلحاق مع ترميز utf-8
file = open("data_1.txt", "a", encoding="utf-8")

# إضافة سطر آخر بالعربية
file.write("\nسليمان القسيمي")

# إغلاق الملف
file.close()


# فتح الملف بوضع القراءة "r" مع ترميز utf-8
file = open("data_1.txt", "r", encoding="utf-8")

# قراءة كامل محتوى الملف دفعة واحدة وتخزينه في متغير
content = file.read()

# طباعة المحتوى
print(content)
# بعد القراءة، يكون المؤشر في نهاية الملف
# لنقله إلى البداية نستخدم seek(0)
# الرقم 0 يعني: ارجع إلى أول حرف في الملف
file.seek(0)

# إغلاق الملف
file.close()
file = open("data_1.txt", "r", encoding="utf-8")

# قراءة السطر الأول فقط
line1 = file.readline()
print("السطر الأول:", line1)
# قراءة السطر الثاني
line2 = file.readline()
print("السطر الثاني:", line2)
# قراءة السطر الثالث
line3 = file.readline()
print("السطر الثالث:", line3)
# قراءة السطر الرابع
line4 = file.readline()
print("السطر الرابع:", line4)
# إذا لم تبق أسطر، تعيد readline() نصاً فارغاً ''
line5 = file.readline()
print("السطر الخامس (فارغ إذا انتهى الملف):", repr(line5))


# نقل المؤشر إلى البداية
file.seek(0)

# قراءة كل الأسطر وتخزينها في قائمة
# كل سطر يصبح عنصراً في القائمة
lines = file.readlines()

# الوصول إلى السطر الأول باستخدام الفهرس 0
print("أول سطر:", lines[0])
# الوصول إلى آخر سطر باستخدام الفهرس -1
print("آخر سطر:", lines[-1])
# إغلاق الملف
file.close()
