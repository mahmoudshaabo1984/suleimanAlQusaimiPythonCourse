# برنامج حساب ضريبة القيمة المضافة 
# الطريقة الثالثة لكتابة الكود
# تحويل المدخل إلى عدد عشري لقبول الكسور
amount = float(input("ادخل المبلغ الذي تريد حساب الضريبة المضافة عليه: \n"))
# نسبة الضريبة
tax_rate = 0.05
# قيمة الضريبة وحدها
tax = amount * tax_rate
# المبلغ الإجمالي
total = amount + tax
print("قيمة الضريبة هي:", tax)
print("الإجمالي بعد الضريبة:", total)