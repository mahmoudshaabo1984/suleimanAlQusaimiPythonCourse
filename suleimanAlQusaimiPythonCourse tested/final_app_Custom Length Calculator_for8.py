# البرنامج النهائي محاكاة دالة حساب الطول len()
# أولاً: تعريف الدالة length التي تحسب الطول (يتم حفظها في الذاكرة لاستخدامها لاحقاً)
def length(o):
    # تهيئة العداد ليبدأ من الصفر
    count = 0
    # حلقة تكرار تمر على كل عنصر (i) موجود داخل القائمة (o)
    for i in o:
        # زيادة العداد بمقدار 1 مع كل عنصر تمر عليه الحلقة
        count = count + 1
    # إرجاع القيمة النهائية للعداد (التي تمثل عدد العناصر)
    return count


# ثانياً: تعريف قائمة (List) تحتوي على أرقام لتجربة الدالة عليها
x = [1, 3, 5, 6]

# ثالثاً: استدعاء الدالة وتمرير القائمة x لها وطباعة النتيجة
# النتيجة التي ستظهر هي 4 (لأن القائمة تحتوي على 4 أرقام)
print(length(x))
