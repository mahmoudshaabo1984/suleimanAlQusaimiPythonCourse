# البرنامج الثاني: جدول الضرب باستخدام الحلقات المتداخلة
# Program 2: Multiplication table using nested while loops
# فتح ملف باسم table.txt بوضع الكتابة w
# سيتم كتابة جدول الضرب كاملاً داخل هذا الملف
file = open("table.txt", "w")
# تعيين قيمة البداية للرقم الأول (الحلقة الخارجية)
x = 1
# تعيين قيمة البداية للرقم الثاني (الحلقة الداخلية)
# نجعل y تساوي x لأن جدول الضرب يبدأ من الرقم نفسه
# مثلاً: جدول 3 يبدأ من 3 × 3 وليس 3 × 1
y = x
# الحلقة الخارجية: تتحكم بالرقم الأول (من 1 إلى 12)
while x <= 12:
    # الحلقة الداخلية: تتحكم بالرقم الثاني (من x إلى 12)
    # تتكرر بالكامل في كل دورة من الحلقة الخارجية
    while y <= 12:
        # كتابة عملية الضرب في الملف باستخدام f-string
        # مثال: "3 x 4 = 12\n"
        # الحرف \n ينقلنا إلى سطر جديد بعد كل عملية
        file.write(f"{x} x {y} = {x*y}\n")
        # زيادة الرقم الثاني بمقدار 1 للانتقال للعملية التالية
        y = y + 1
    # بعد انتهاء الحلقة الداخلية: زيادة الرقم الأول بمقدار 1
    x = x + 1
    # إعادة تعيين y لتساوي x الجديدة
    # هذا يمنع تكرار العمليات (مثلاً 3×4 و 4×3)
    y = x
# إغلاق الملف بعد كتابة كامل جدول الضرب
file.close()

print("تم إنشاء ملف table.txt بنجاح وكتابة جدول الضرب فيه!")
print("افتح الملف table.txt لمشاهدة النتيجة.")


# البرنامج الثالث: قاعدة بيانات درجات الطلاب

# فتح ملف البيانات grades_data.txt بوضع القراءة r مع ترميز utf-8
# هذا الملف يحتوي على أسماء الطلاب ودرجاتهم مفصولة بمسافة
# كل سطر فيه: اسم_الطالب درجته
file = open("grades_data.txt", "r", encoding="utf-8")
# قراءة كامل محتوى الملف كنص واحد
content = file.read()
# تقسيم النص إلى قائمة من الأسطر بناءً على علامة السطر الجديد \n
# كل عنصر في القائمة يمثل سطراً واحداً (طالب واحد)
content = content.split("\n")
# إنشاء قاموس فارغ لتخزين بيانات الطلاب
# القاموس يربط كل اسم طالب بدرجته
data = {}
# المرور على كل سطر في قائمة الأسطر
for line in content:
    # تقسيم السطر الواحد إلى قائمة بناءً على المسافة
    # مثال: "علي 97" تصبح ["علي", "97"]
    line = line.split("\t")
    # العنصر الأول (فهرس 0) هو اسم الطالب
    name = line[0]
    # العنصر الثاني (فهرس 1) هو درجة الطالب
    grade = line[1]
    # إضافة الاسم والدرجة إلى القاموس
    # الاسم يكون المفتاح (key) والدرجة تكون القيمة (value)
    data[name] = grade

# إغلاق الملف بعد الانتهاء من قراءته
file.close()
# طباعة قائمة بأسماء جميع الطلاب المتوفرين
print("قائمة الطلاب: ")

# المرور على جميع المفاتيح (الأسماء) في القاموس وطباعتها
for key in data.keys():
    print(key)

# حلقة لا نهائية تسمح للمستخدم بالبحث المتكرر عن درجات الطلاب
# تستمر حتى يوقفها المستخدم يدوياً بالضغط على Ctrl+C
while True:
    # طلب إدخال اسم الطالب من المستخدم
    name = input("اكتب اسم الطالب الذي تريد معرفة درجته: ")
    # محاولة البحث عن الاسم في القاموس
    try:
        # إذا وُجد الاسم: طباعة درجته باستخدام f-string
        print(f"درجة {name} هي {data[name]}")
    # إذا لم يُوجد الاسم: يحدث خطأ KeyError
    except KeyError:
        # طباعة رسالة تخبر المستخدم أن الاسم غير موجود
        print("الاسم غير موجود")
