






# انشاء برنامج مولد كلمات مرور عشوائية password generator
# استيراد دالة choice من مكتبة random (تستخدم لاختيار عنصر عشوائي من قائمة)
from random import choice
# تعريف متغير يحتوي على كل الحروف (صغيرة وكبيرة) والأرقام والرموز التي ستتكون منها كلمة المرور
choices = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%^&*()_+"
# طلب إدخال طول كلمة المرور من المستخدم وتخزينها في المتغير length
length = input("أدخل طول كلمة المرور الجديدة: ")
# تحويل القيمة المدخلة من نص (String) إلى رقم صحيح (Integer) لاستخدامه في العد
length = int(length)
# إنشاء متغير فارغ سيتم تخزين كلمة المرور النهائية فيه
password = ""
# تعريف عداد يبدأ من 1 للتحكم في عدد مرات التكرار
count = 1
# بدء حلقة تكرار (Loop) تستمر حتى يصل العداد إلى الطول المطلوب
while count <= length:
    # اختيار حرف عشوائي واحد من قائمة الحروف والرموز (choices)
    c = choice(choices)
    # إضافة الحرف العشوائي المختار إلى متغير كلمة المرور
    password = password + c
    # زيادة العداد بمقدار 1 للانتقال للدورة التالية
    count = count + 1

# طباعة كلمة المرور النهائية بعد انتهاء الحلقة (هذا السطر مفقود في الصورة ولكنه ضروري لرؤية النتيجة)
print("كلمة المرور المقترحة هي:", password)





    
